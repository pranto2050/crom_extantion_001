// Import Dexie and background sync module
// FIX [H14]: Track whether scripts loaded successfully
let _scriptsLoaded = false;
let _scriptLoadError = null;

if (typeof importScripts === 'function') {
    try {
        importScripts('lib/dexie.js');
        importScripts('background-sync.js');
        importScripts('background/review-prompts.js');
        importScripts('background/quick-save.js');
        importScripts('background/title-fetch.js');
        _scriptsLoaded = true;
    } catch (e) {
        console.error('Failed to import scripts:', e);
        _scriptLoadError = e.message || 'Unknown error loading scripts';
    }
} else {
    // Firefox packaged build can run background scripts in document scope (no importScripts).
    // In that mode, Dexie, BackgroundSync, and helper background modules are expected to be
    // loaded via manifest script order generated by the Firefox release packager.
    const hasDexie = typeof Dexie !== 'undefined';
    const hasBackgroundSync = typeof BackgroundSync !== 'undefined';
    const hasReviewPromptHelpers = typeof resolveReviewPromptUserId === 'function'
        && typeof requestReviewPromptDisplay === 'function'
        && typeof applyReviewPromptAction === 'function';
    const hasQuickSaveHelpers = typeof quickSaveCurrentTab === 'function'
        && typeof saveAllTabs === 'function';
    const hasTitleFetchHelpers = typeof fetchPageTitle === 'function'
        && typeof fetchFaviconLinksFromPage === 'function'
        && typeof fetchFaviconAsBase64 === 'function';
    _scriptsLoaded = hasDexie
        && hasBackgroundSync
        && hasReviewPromptHelpers
        && hasQuickSaveHelpers
        && hasTitleFetchHelpers;
    if (!_scriptsLoaded) {
        _scriptLoadError = 'Required background globals missing in non-worker background context';
        console.error(_scriptLoadError);
    }
}

